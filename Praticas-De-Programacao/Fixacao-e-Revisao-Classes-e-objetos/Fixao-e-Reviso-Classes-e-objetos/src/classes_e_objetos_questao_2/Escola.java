package classes_e_objetos_questao_2;

public class Escola {
	
	private String nomeEscola;
	private int capacidadeSalas;
	private String cnpj;
	
	
	
	public String getNomeEscola() {
		return nomeEscola;
	}
	public void setNomeEscola(String nomeEscola) {
		this.nomeEscola = nomeEscola;
	}
	public int getCapacidadeSalas() {
		return capacidadeSalas;
	}
	public void setCapacidadeSalas(int capacidadeSalas) {
		this.capacidadeSalas = capacidadeSalas;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	
}
