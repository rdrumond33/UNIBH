package jogo_da_forca;


public class Jogador {

    private String Nome_jogador;
    private String Codigo_jogador;
    private int Pontuacao_jogador;

    
    
    
    /**
     * @return the Nome_jogador
     */
    public String getNome_jogador() {
        return Nome_jogador;
    }

    /**
     * @param Nome_jogador the Nome_jogador to set
     */
    public void setNome_jogador(String Nome_jogador) {
        this.Nome_jogador = Nome_jogador;
    }

    /**
     * @return the Codigo_jogador
     */
    public String getCodigo_jogador() {
        return Codigo_jogador;
    }

    /**
     * @param Codigo_jogador the Codigo_jogador to set
     */
    public void setCodigo_jogador(String Codigo_jogador) {
        this.Codigo_jogador = Codigo_jogador;
    }

    /**
     * @return the Pontuacao_jogador
     */
    public int getPontuacao_jogador() {
        return Pontuacao_jogador;
    }

    /**
     * @param Pontuacao_jogador the Pontuacao_jogador to set
     */
    public void setPontuacao_jogador(int Pontuacao_jogador) {
        this.Pontuacao_jogador = Pontuacao_jogador;
    }
    

    
    
    
    
    
}
